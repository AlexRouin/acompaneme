CREATE TABLE RAZGOS
(
USUARIO VARCHAR(18) PRIMARY KEY NOT NULL ,
FOREIGN KEY (USUARIO) REFERENCES USUARIO (USUARIO),
ALTURA VARCHAR (4),
PESO VARCHAR (8),
COMPLEXION VARCHAR(10),
C_OJOS  VARCHAR(10),
C_CABELLO VARCHAR(10),
RAZA VARCHAR(20)
);


